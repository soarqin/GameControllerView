// PlayStation controller axes (shared between PS3, PS4, PS5)
var playStationAxes = []AxisMapping{
	{Index: 0, Target: "left_x"},
	{Index: 1, Target: "left_y", Invert: true},
	{Index: 2, Target: "right_x"},
	{Index: 3, Target: "right_y", Invert: true},
	{Index: 4, Target: "lt", IsTrigger: true, RawMin: -32768, RawMax: 32767},
	{Index: 5, Target: "rt", IsTrigger: true, RawMin: -32768, RawMax: 32767},
}

// PlayStation controller base buttons (shared between PS3, PS4, PS5)
var playStationBaseButtons = []ButtonMapping{
	{Index: 0, Target: "a"},     // Cross (×)
	{Index: 1, Target: "b"},     // Circle (○)
	{Index: 2, Target: "x"},     // Square (□)
	{Index: 3, Target: "y"},     // Triangle (△)
	{Index: 4, Target: "back"},  // Share / Create
	{Index: 5, Target: "guide"}, // PS button
	{Index: 6, Target: "start"}, // Options
	{Index: 7, Target: "ls"},
	{Index: 8, Target: "rs"},
	{Index: 9, Target: "lb"},  // L1
	{Index: 10, Target: "rb"}, // R1
}

// newPlayStationMapping creates a PlayStation mapping with optional extra buttons.
// PS5 adds button 11 (touchpad), PS3/PS4 don't have it.
func newPlayStationMapping(name string, extraButtons ...ButtonMapping) *DeviceMapping {
	buttons := make([]ButtonMapping, len(playStationBaseButtons)+len(extraButtons))
	copy(buttons, playStationBaseButtons)
	copy(buttons[len(playStationBaseButtons):], extraButtons)

	return &DeviceMapping{
		Name:    name,
		Axes:    playStationAxes,
		Buttons: buttons,
		HasHat:  true,
	}
}

